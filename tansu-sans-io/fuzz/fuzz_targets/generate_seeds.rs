    // VarInt(i32::MAX) zigzag = 0xFFFFFFFE => FE FF FF FF 0F
    write_seed(dir, "i32_max", &[0xFE, 0xFF, 0xFF, 0xFF, 0x0F]);
    // VarInt(i32::MIN) zigzag = 0xFFFFFFFF => FF FF FF FF 0F
    write_seed(dir, "i32_min", &[0xFF, 0xFF, 0xFF, 0xFF, 0x0F]);
    // Empty input — should trigger error path
    write_seed(dir, "empty", &[]);
    // All continuation bits set (never terminates within 5 bytes) — overflow
    write_seed(
        dir,
        "all_continuation",
        &[0x80, 0x80, 0x80, 0x80, 0x80, 0x80],
    );
    // Single continuation byte
    write_seed(dir, "single_continuation", &[0x80]);
    // Two bytes
    write_seed(dir, "two_bytes", &[0xAC, 0x02]);
    // LongVarInt(i64::MAX) zigzag = 0xFFFFFFFFFFFFFFFE
    write_seed(
        dir,
        "i64_max",
        &[0xFE, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x01],
    );
    // LongVarInt(i64::MIN) zigzag = 0xFFFFFFFFFFFFFFFF
    write_seed(
        dir,
        "i64_min",
        &[0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x01],
    );
    // Random multi-byte
    write_seed(dir, "random_multi", &[0xD2, 0x85, 0xD8, 0xCC, 0x04]);

